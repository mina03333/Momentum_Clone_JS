const calculator = {
    add: function (a, b) {
        console.log(a + b)
    },
    subtract: function (a, b) {
        console.log(a - b)
    },
    divide: function (a, b) {
        console.log(a / b)
    },
    powerOf: function (a, b) {
        console.log(a ** b)
    }
}

calculator.add(9, 8);
calculator.subtract(9, 8);
calculator.divide(9, 8);
calculator.powerOf(9, 8);